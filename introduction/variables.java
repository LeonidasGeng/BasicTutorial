public class variables {
    public static void main(String[] args) {
        //数据类型 变量名 = 数据值；
        //整数 int
        //小数/浮点数 double



        //定义一个整数类型的变量
        //把数据10复制给了a,a储存了数据10
        int a = 10;
        System.out.println(a);//10

        //定义一个小数类型的变量
        double b = 20;
        System.out.println(b);//20
    }
}
